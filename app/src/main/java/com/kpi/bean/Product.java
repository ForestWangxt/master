package com.kpi.bean;


public class Product {
    private String productName;
    private String productCount;
    private String productMom;
    private String productCustomer;
    private String productCustomerMom;

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getProductCount() {
        return productCount;
    }

    public void setProductCount(String productCount) {
        this.productCount = productCount;
    }

    public String getProductMom() {
        return productMom;
    }

    public void setProductMom(String productMom) {
        this.productMom = productMom;
    }

    public String getProductCustomer() {
        return productCustomer;
    }

    public void setProductCustomer(String productCustomer) {
        this.productCustomer = productCustomer;
    }

    public String getProductCustomerMom() {
        return productCustomerMom;
    }

    public void setProductCustomerMom(String productCustomerMom) {
        this.productCustomerMom = productCustomerMom;
    }


}
